package model.entitiesutil;

/**
 * All the directions an {@link Entity} can move
 */
public enum EntityDirections{

	/**
	 * Left direction
	 */
	LEFT,

	/**
	 * Right direction
	 */
	RIGHT,

	/**
	 * Up direction
	 */
	UP,

	/**
	 * Down direction
	 */
	DOWN,

	/**
	 * Bottom left direction
	 */
	DOWN_LEFT,

	/**
	 * Bottom right direction
	 */
	DOWN_RIGHT

}
